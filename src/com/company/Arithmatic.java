package com.company;

public class Arithmatic {
    public static void main(String[] args) {
        int a = 5;
        int b = 4;

        System.out.println("a = "+ a + " and b = "+ b);
        System.out.println("Sum = "+ (a+b) );
        System.out.println("Minus = "+ (a-b) );
        System.out.println("Multi = "+ (a*b) );
        System.out.println("Div = "+ a/b );
    }
}
